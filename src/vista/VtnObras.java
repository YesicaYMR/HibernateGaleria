    private void btnCrearActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCrearActionPerformed
        if (comprobarDatos()) {
            String nombre = txtNombre.getText();
            Autor c = (Autor) cmbAutor.getSelectedItem();
            String tipo = cmbTipo.getSelectedItem().toString();
            //crea la obra en la bbdd
            boolean ok = operacionesObras.crearObra(nombre, tipo, c);
            if (ok) {
                JOptionPane.showMessageDialog(this, "Obra creada exitosamente");
                limpiar();
            } else {
                JOptionPane.showMessageDialog(this, "Ha surgido un error");
            }
        }

    }//GEN-LAST:event_btnCrearActionPerformed


