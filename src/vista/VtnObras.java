    /**
     * Verifica si los datos ingresados son válidos.
     *
     * @return true si los datos son correctos, false de lo contrario.
     */
    private boolean comprobarDatos() {
        boolean correcto = false;
        String nombre = txtNombre.getText();
        //Comprobar si nombre tiene texto
        if (nombre.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Debe introducir un nombre");
            txtNombre.requestFocus();
        } //comprobar si ha elegido autor
        else if (cmbAutor.getSelectedIndex() == 0) {
            JOptionPane.showMessageDialog(this, "Debe elegir un autor");
        } //comprobar si ha elegido un tipo
        else if (cmbTipo.getSelectedIndex() == 0) {
            JOptionPane.showMessageDialog(this, "Elija una categoría");
        } else {
            correcto = true;
        }
        return correcto;
    }

    /**
     * Limpia los campos de entrada de texto y restablece los ComboBox a sus
     * valores predeterminados.
     */
    private void limpiar() {
        txtNombre.setText("");
        cmbAutor.setSelectedIndex(0);
        cmbTipo.setSelectedIndex(0);
    }

    /**
     * Carga los autores disponibles en el ComboBox de autores.
     */
    private void cargarComboAutores() {
        modeloAutores = new DefaultComboBoxModel();
        cmbAutor.setModel(modeloAutores);
        modeloAutores.addElement("--Seleccione un autor--");
        //Ejecutar busqueda
        List<Autor> autores = operacionesAutor.obtenerTodosLosAutores();
        //añadirlos al combo
        for (Autor autor : autores) {
            modeloAutores.addElement(autor);
        }
    }

    /**
     * Carga los tipos de arte disponibles en el ComboBox de tipos de arte.
     */
    private void cargarComboTipoArte() {
        modeloTipoArte = new DefaultComboBoxModel();
        cmbTipo.setModel(modeloTipoArte);
        modeloTipoArte.addElement("--Seleccione un tipo--");
        modeloTipoArte.addElement("Pintura");
        modeloTipoArte.addElement("Escultura");
        modeloTipoArte.addElement("Fotografía");
        modeloTipoArte.addElement("Grabado");
    }

